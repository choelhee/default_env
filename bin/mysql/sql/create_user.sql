
-- AUTH_OPTION
DROP USER IF EXISTS USER1;
CREATE USER IF NOT EXISTS
	USER1
	-- AUTH OPTION
	IDENTIFIED BY 'PASSWD';


-- TLS_OPTION
DROP USER IF EXISTS USER2;
CREATE USER IF NOT EXISTS USER2@'localhost' IDENTIFIED BY 'passwd'
	REQUIRE NONE
;

DROP USER IF EXISTS USER3;
CREATE USER IF NOT EXISTS USER3@'localhost' IDENTIFIED BY 'passwd'
	REQUIRE SSL
;

DROP USER IF EXISTS USER4;
CREATE USER IF NOT EXISTS USER4@'localhost' IDENTIFIED BY 'passwd'
	REQUIRE CIPHER 'CIPHER' 
;

DROP USER IF EXISTS USER5;
CREATE USER IF NOT EXISTS USER5@'localhost' IDENTIFIED BY 'passwd'
	REQUIRE ISSUER 'ISSUE'
;

DROP USER IF EXISTS USER6;
CREATE USER IF NOT EXISTS USER6@'localhost' IDENTIFIED BY 'passwd'
	REQUIRE SUBJECT 'SUBJECT'
;

DROP USER IF EXISTS USER7;
CREATE USER IF NOT EXISTS USER7@'localhost' IDENTIFIED BY 'passwd'
	REQUIRE X509
;

DROP USER IF EXISTS USER8;
CREATE USER IF NOT EXISTS USER8@'localhost'
  REQUIRE SUBJECT '/C=SE/ST=Stockholm/L=StockholmO=MySQL demo client certificateCN=client/emailAddress=client@example.com'
      AND ISSUER  '/C=SE/ST=Stockholm/L=StockholmO=MySQL/CN=CA/emailAddress=ca@example.com'
      AND CIPHER  'EDH-RSA-DES-CBC3-SHA';




-- PASSWORD OPTION
DROP USER IF EXISTS USER9;
CREATE USER IF NOT EXISTS USER9@'localhost' IDENTIFIED BY 'passwd'
	PASSWORD EXPIRE	
;

DROP USER IF EXISTS USER10;
CREATE USER IF NOT EXISTS USER10@'localhost' IDENTIFIED BY 'passwd'
	PASSWORD EXPIRE DEFAULT	
;

DROP USER IF EXISTS USER11;
CREATE USER IF NOT EXISTS USER11@'localhost' IDENTIFIED BY 'passwd'
	PASSWORD EXPIRE NEVER
;

DROP USER IF EXISTS USER12;
CREATE USER IF NOT EXISTS USER12@'localhost' IDENTIFIED BY 'passwd'
	PASSWORD EXPIRE INTERVAL 1 DAY
;


-- RESOURCE OPTION
DROP USER IF EXISTS USER13;
CREATE USER IF NOT EXISTS USER13@'localhost' IDENTIFIED BY 'passwd'
	WITH MAX_QUERIES_PER_HOUR 1
		MAX_UPDATES_PER_HOUR 1
		MAX_CONNECTIONS_PER_HOUR 1
		MAX_USER_CONNECTIONS 1
;



