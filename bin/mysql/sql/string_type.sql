CREATE TABLE S5 (
	C1 BINARY(5),
	C2 VARBINARY(5)
	);

SELECT 
LENGTH( RPAD( 'A', 4, 'A' ) ), LENGTH( RPAD( 'A', 5, 'A' ) )
 FROM DUAL;

