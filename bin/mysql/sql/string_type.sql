

-- char varchar 
DROP TABLE IF EXISTS S4;
CREATE TABLE S4 (
	C1 CHAR(5),
	C2 VARCHAR(5)
	);
INSERT INTO S4 VALUES( 'A   ', 'A   ' );

-- WHERE CONDITION
SELECT 
	CONCAT( '(', C1 , ')' ),
	CONCAT( '(', C2 , ')' ) FROM S4;

SELECT 'CHAR NO   SPACE CONDITION' FROM S4 WHERE C1 = 'A';
SELECT 'CHAR WITH SPACE CONDITION' FROM S4 WHERE C1 = 'A   ';
SELECT 'VARCHAR NO   SPACE CONDITION' FROM S4 WHERE C2 = 'A';
SELECT 'VARCHAR WITH SPACE CONDITION' FROM S4 WHERE C2 = 'A   ';



-- BINARY VARBINARY
