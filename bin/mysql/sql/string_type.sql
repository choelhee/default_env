SELECT LENGTH( C1 ), LENGTH(C2) FROM S5;

DELETE FROM S5;
INSERT INTO S5 VALUES( 'A', 'B' );

-- WHERE CONDITION
SELECT HEX(C1), C1 = 'A' , C1 = 'A\0\0\0\0' FROM S5;
SELECT 'WHERE CONDITION' FROM S5 WHERE C1 = 'A\0\0\0\0';
SELECT 'WHERE CONDITION' FROM S5 WHERE C1 = 'A';

