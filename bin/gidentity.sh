


$GSQL --no-prompt <<EOF

SELECT
    I.TABLE_SCHEMA,
    I.TABLE_NAME,
    I.COLUMN_NAME,
    s.LOCAL_CURR_VALUE,
    S.LOCAL_NEXT_VALUE
FROM
    USER_TAB_IDENTITY_COLS     I,
    DEFINITION_SCHEMA.SCHEMATA D,
    DEFINITION_SCHEMA.COLUMNS  C,
    WHOLE_TABLES T,
    X\$SEQUENCE S
WHERE
    I.TABLE_SCHEMA         = D.SCHEMA_NAME 
    AND I.TABLE_NAME           = T.TABLE_NAME
    AND I.COLUMN_NAME          = C.COLUMN_NAME
    AND D.SCHEMA_ID            = T.SCHEMA_ID 
    AND D.SCHEMA_ID            = C.SCHEMA_ID
    AND T.TABLE_ID             = C.TABLE_ID
    AND C.IDENTITY_PHYSICAL_ID = S.PHYSICAL_ID
        ;


EOF
